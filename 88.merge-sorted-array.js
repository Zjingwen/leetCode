/**
 * @param {number[]} nums1
 * @param {number} m
 * @param {number[]} nums2
 * @param {number} n
 * @return {void} Do not return anything, modify nums1 in-place instead.
 */
var merge = function(nums1, m, nums2, n) {
  for(let i = 0;i<nums1.length;i++){
    if(i>=m){
      nums1[i] = nums2[i-m]
    };
  }
  for(let i = 0;i<nums1.length;i++){
    if(nums1[i+1] && nums1 > nums1[i+1]){
      let i = nums1;
      nums1 = nums1[i+1];
      nums1[i+1] = i;
    }
  }
  function bubbleSort(arr) {
    var len = arr.length;
    for (var i = 0; i < len; i++) {
        for (var j = 0; j < len - 1 - i; j++) {
            if (arr[j] > arr[j+1]) {
                var temp = arr[j+1];
                arr[j+1] = arr[j];
                arr[j] = temp;
            }
        }
    }
    return arr;
  }
  return bubbleSort(nums1);
};
